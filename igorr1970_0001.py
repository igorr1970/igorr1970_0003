# ДЗ Строки и индексация строк
example = 'Двигатель'
print(example)
print(example[0]) # первый символ этой строки Ответ: Д
print(example[-1]) # последний символ этой строки (используя отрицательный индекс) Ответ: ь
print(example[4:]) # вторую половину этой строки (С нечётным количеством символов) Ответ: атель
print(example[::-1]) #  это слово наоборот Ответ: ьлетагивД
print(example[1:8:2]) # каждый второй символ этой строки Ответ: вгтл